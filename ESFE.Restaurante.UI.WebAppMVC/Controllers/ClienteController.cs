        [HttpPost]
        public JsonResult GuardarCliente(ClienteEN pClienteEN)
        {
            string mensaje = "";
            if (pClienteEN != null)
            {
                if (_clienteBL.GuardarCliente(pClienteEN) > 0)
                {
                    mensaje = "¡Registro guardado!";
                }
                else
                {
                    mensaje = "No se guardo el registro";
                }
            }
            else
            {
                mensaje = "No dejar campos vacios";
            }
            return Json(new { Mensaje = mensaje, JsonRequestBehavior.AllowGet });
          
        }
        //----------------------------------------------------------------------------------------
        public JsonResult ClienteJs(ClienteEN pClienteEN)
        {

            string mensaje = "";
            if (pClienteEN.Nombre != null && pClienteEN.Apellido != null && pClienteEN.Dui != null && pClienteEN.Gmail != null && pClienteEN.Telefono != null)
            {
               if (pClienteEN.Nombre.Length > 0 && pClienteEN.Apellido.Length > 0 && pClienteEN.Dui.Length > 0 && pClienteEN.Gmail.Length > 0 && pClienteEN.Telefono.Length > 0)
               {
                    List<ClienteEN> ListaC = _clienteBL.ValidarCliente(pClienteEN);
                    if (ListaC.Count > 0)
                    {
                        long ClienteId = ListaC.FirstOrDefault().Id;
                        return Json(new { ClienId = ClienteId, JsonRequestBehavior.AllowGet });

                    }
                    else
                    {
                        mensaje = "Tus datos no existen";
                    }
                }
               else
                {
                    mensaje = "Tus datos no existen";
                }
            }
            else
            {
                mensaje = "No dejar campos vacios";
            }
            return Json(new { Mensaje = mensaje, JsonRequestBehavior.AllowGet });

        }

